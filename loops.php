<?php
$n = 5;
while ($n < 5) {
    echo $n . PHP_EOL;
    $n += 1;
}

// Looping from i = 0 to i = 4
for ($i = 0; $i < 5; $i++) {
    echo $i . PHP_EOL;
}

// Looping from i = 2 to i = 5
for ($i = 2; $i < 6; $i++) {
    echo $i . PHP_EOL;
}

// Looping from i = 5 to i = 2
for ($i = 5; $i >= 2; $i--) {
    echo $i . PHP_EOL;
}
